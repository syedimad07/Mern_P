// logical operators

//  {Logical And &&}
var age = 66;
var result = age>=18 && age<=65;
console.log(result);
result : false
var age = 50;
var result = age>=18 && age<=65;
console.log(result);
//result : true


// {Logical OR ||}
var age = 66;
var result = age>=18 || age<=65;
console.log(result);
result = true

var age = 60;
var result = age>=18 || age<=61 || age<=50
console.log(result);
//result = true

// {Logical Not !}
var age = 60
var result = age>=18 || age<=61;
console.log(!result)
//result = false
